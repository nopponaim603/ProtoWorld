namespace Cariacity.game
{
    public class Rectangle
    {
        public int top;
        public int left;
        public int right;
        public int bottom;

        public Rectangle(int left, int top, int right, int bottom)
        {
            this.top = top;
            this.left = left;
            this.right = right;
            this.bottom = bottom;
        }
    }
}
